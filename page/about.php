			<h4 class="title">Industrial Design Centre</h4>
			<p>
				The Industrial Design Centre (IDC) is one of the oldest and most distinguished design 
				schools in India, located within the Indian Institute of technology, Mumbai, India (IIT Bombay)
			</p>
			<p class="meta-one">Since 1969, IDC offers an excellent environment for academics, research projects in the field of design.</p>
			<p>
				The education programme at IDC is an unique mix of pedagogic experimnetation with a pragmatic
				design appraoch. It blends hard core problem-solving with design research. New thoughts, philosophies and 
				research into several aspects of design are experimented with to continuously revitalize the academic 
				programmes at IDC. 
			</p>
			<p>
				IDC graduates have contributed to every major field of design, all across the world. Alumni from 
				IDC have been key players in eminent companies and research labs including <span class="meta-two">Google, Microsoft, Yahoo,
				Lego, Volvo, Samsung, Zynga and MIT Media Labs</span>.
			</p>
